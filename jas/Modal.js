// 2. Кешируем элементы модалки
const modal     = document.getElementById('modal-info');
const overlay   = modal.querySelector('.modal__overlay');
const btnClose  = modal.querySelector('.modal__close');
const modalBody = modal.querySelector('.modal__body');

// 3. Обработчик клика по кнопкам
document.querySelectorAll('.js-open-modal').forEach(btn => {
    btn.addEventListener('click', () => {
        const key  = btn.dataset.service;
        const data = servicesData[key];
        if (!data) {
            console.warn(`No data for service "${key}"`);
            return;
        }

        // собираем HTML списка цен
        const pricesHtml = data.prices
            .map(p => `<li>${p.label} — ${p.value}</li>`)
            .join("");

        // заполняем тело модального окна (только картинка + описание + прайс)
        modalBody.innerHTML = `
      <div class="modal__img">
        <img src="${data.img}" alt="">
      </div>
      <div class="modal__text">
        ${data.description}
        <div class="modal__prices">
          <ul>${pricesHtml}</ul>
        </div>
      </div>
    `;

        // показываем модалку и блокируем скролл страницы
        modal.classList.add('is-open');
        document.body.classList.add('modal-open');
    });
});

// 4. Закрытие модалки
function closeModal() {
    modal.classList.remove('is-open');
    document.body.classList.remove('modal-open');
}
btnClose.addEventListener('click', closeModal);
overlay.addEventListener('click', closeModal);
document.addEventListener('keydown', e => {
    if (e.key === 'Escape' && modal.classList.contains('is-open')) {
        closeModal();
    }
});
